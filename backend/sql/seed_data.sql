DELIMITER $$
CREATE PROCEDURE add_column_if_not_exists()
BEGIN
  IF NOT EXISTS (SELECT * FROM information_schema.COLUMNS 
                 WHERE TABLE_SCHEMA = 'wudong_tourism' AND TABLE_NAME = '`order`' AND COLUMN_NAME = 'cancel_policy') THEN
    ALTER TABLE `order` ADD COLUMN `cancel_policy` VARCHAR(512) NULL COMMENT '退订政策说明（预留字段）' AFTER `status`;
  END IF;
  IF NOT EXISTS (SELECT * FROM information_schema.COLUMNS 
                 WHERE TABLE_SCHEMA = 'wudong_tourism' AND TABLE_NAME = '`order`' AND COLUMN_NAME = 'cancel_deadline') THEN
    ALTER TABLE `order` ADD COLUMN `cancel_deadline` DATETIME NULL COMMENT '最晚免费取消时间（预留字段）' AFTER `cancel_policy`;
  END IF;
END$$
DELIMITER ;
